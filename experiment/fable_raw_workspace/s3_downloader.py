import os
import boto3
import concurrent.futures

def download_file(bucket_name: str, object_key: str, download_path: str, s3_client) -> bool:
    """Download a single file from S3."""
    local_file_path = os.path.join(download_path, os.path.basename(object_key))
    
    try:
        os.makedirs(os.path.dirname(local_file_path), exist_ok=True)
        s3_client.download_file(bucket_name, object_key, local_file_path)
        print(f"Downloaded {object_key} to {local_file_path}")
        return True
    except Exception as e:
        print(f"Error downloading {object_key}: {e}")
        return False

def download_images_in_parallel(bucket_name: str, prefix: str, download_path: str, max_workers: int = 50):
    """Download up to 10,000 S3 image objects in parallel."""
    s3_client = boto3.client('s3')
    
    paginator = s3_client.get_paginator('list_objects_v2')
    pages = paginator.paginate(Bucket=bucket_name, Prefix=prefix)
    
    object_keys = []
    for page in pages:
        if 'Contents' in page:
            for obj in page['Contents']:
                key = obj['Key']
                if key.lower().endswith(('.png', '.jpg', '.jpeg', '.gif', '.webp')):
                    object_keys.append(key)
                
                if len(object_keys) >= 10000:
                    break
        if len(object_keys) >= 10000:
            break

    print(f"Found {len(object_keys)} images to download.")

    successful_downloads = 0
    with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
        future_to_key = {
            executor.submit(download_file, bucket_name, key, download_path, s3_client): key 
            for key in object_keys
        }
        
        for future in concurrent.futures.as_completed(future_to_key):
            key = future_to_key[future]
            try:
                success = future.result()
                if success:
                    successful_downloads += 1
            except Exception as e:
                print(f"Exception generated by {key}: {e}")

    print(f"Successfully downloaded {successful_downloads}/{len(object_keys)} images.")

if __name__ == "__main__":
    BUCKET_NAME = 'your-s3-bucket-name'
    PREFIX = 'images/'
    DOWNLOAD_PATH = './downloaded_images'
    
    download_images_in_parallel(BUCKET_NAME, PREFIX, DOWNLOAD_PATH, max_workers=50)
